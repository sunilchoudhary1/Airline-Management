    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        String FirstName = firstName.getText();
        String LastName = lastName.getText();
        String Passport = passport.getText();
        String Nic = nic.getText();
        String Address = address.getText();
        Date d = (Date) dob.getDate();
        String Gender = "";
        if(male.isSelected())
        {
            Gender = "Male";
        }
        else
        {
            Gender = "Female";
        }
        String Contact = contact.getText();
        String CustomerID = customerid.getText();
        DateFormat dt = new SimpleDateFormat("yyyy-mm-dd");
        String date = dt.format(d);
        try {                                         
            // TODO add your handling code here:
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3307/airlinedatabase","root","Sunil@123");
        pre = con.prepareStatement("insert into Customers(idCustomer,FirstName,LastName,Passport,NIC_ID,Address,DOB,Gender,Contact) values(?,?,?,?,?,?,?,?,?)");
        pre.setString(1, CustomerID);
        pre.setString(2, FirstName);
        pre.setString(3, LastName);
        pre.setString(4, Passport);
        pre.setString(5, Nic);
        pre.setString(6, Address);
        pre.setString(7, date);
        pre.setString(8,Gender);
        pre.setString(9, Contact);
        pre.executeUpdate();
        JOptionPane.showMessageDialog(null, "Customer added");

        } catch (SQLException ex) {
                Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_AddActionPerformed

